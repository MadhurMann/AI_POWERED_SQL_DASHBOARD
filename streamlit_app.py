def make_api_request(endpoint: str, payload: Dict[str, Any]) -> Optional[Dict[str, Any]]:
    """Make API request with proper error handling and timeout."""
    try:
        response = requests.post(
            f"{BASE_URL}/{endpoint}",
            json=payload,
            timeout=REQUEST_TIMEOUT,
            headers={"Content-Type": "application/json"}
        )
        response.raise_for_status()
        return response.json()
    except requests.exceptions.ConnectionError:
        st.error("âŒ Could not connect to the backend server. Please ensure it's running on localhost:8000")
        return None
    except requests.exceptions.Timeout:
        st.error(f"â±ï¸ Request timed out after {REQUEST_TIMEOUT} seconds. Please try again.")
        return None
    except requests.exceptions.HTTPError as e:
        st.error(f"âŒ Server error: {e.response.status_code} - {e.response.text}")
        return None
    except requests.exceptions.RequestException as e:
        st.error(f"âŒ Request failed: {str(e)}")
        return None
    except json.JSONDecodeError:
        st.error("âŒ Invalid response format from server")
        return None

